
/**
 * Enum with direction data.
 * 
 * @author Mayank
 */
public enum Direction {

    /**
     * Direction to move up.
     */
    UP,
    /**
     * Direction to move down.
     */
    DOWN,
    /**
     * Direction to move left.
     */
    LEFT,
    /**
     * Direction to move right.
     */
    RIGHT,
    /**
     * Direction to move up_right.
     */
    UP_RIGHT,
    /**
     * Direction to move up_left.
     */
    UP_LEFT,
    /**
     * Direction to move down_left.
     */
    DOWN_LEFT,
    /**
     * Direction to move down_right.
     */
    DOWN_RIGHT;
}